
public class Empleado {
    String cc;
    String nombre;
    String apellido;
    String telefono;
    String ciudad;
    String direccion;
    public Empleado(String cc,String nombre,String apellido,String telefono, String ciudad,String direccion){
        this.cc=cc;
        this.nombre=nombre;
        this.apellido=apellido;
        this.telefono=telefono;
        this.ciudad=ciudad;
        this.direccion=direccion;
    }
    
}
